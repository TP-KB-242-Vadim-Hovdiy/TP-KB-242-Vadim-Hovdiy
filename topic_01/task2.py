# strip() – прибирає пробіли на початку та в кінці
text1 = "   hello world   "
print("text1():", text1.strip())
# capitalize() – робить першу літеру великою, решта стають маленькими
text2 = "hello world"
print("text2():", text2.capitalize())
# title() – робить першу літеру кожного слова великою
text3 = "hello world"
print("text3():", text3.title())
# upper() – всі букви стають великими
text4 = "hello world"
print("text4():", text4.upper())
# lower() – всі букви стають маленькими
text5 = "Hello World"
print("text5():", text5.lower())